export function typesColors(type) {
  switch (type) {
    case "grass":
      return "green";
      break;
    case "poison":
      return "violet";
      break;
    case "flying":
      return "gray";
      break;
    case "fire":
      return "red";
      break;
    case "rock":
      return "gray";
      break;
    case "water":
      return "blue";
      break;
    default:
      break;
  }
}
